package operator;

public class Assign1 {

    public static void main(String[] args) {
        int a = 5;
        a += 3; // 8 (5 + 3): a = a + 3
        System.out.println(a);
        a -= 2; // 6 (8 - 2)
        System.out.println(a);
        a *= 3; // 18 (6 * 3)
        System.out.println(a);
        a /= 3; // 6 (18 / 3)
        System.out.println(a);
        a %= 5; // 1 (6 % 5)
        System.out.println(a);




    }
}
